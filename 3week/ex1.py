print(df.loc[:, '강의실': '담당교수'], end='\n\n') # : -> 모든 행 선택, 강의실~담당교수까지의 열 선택

print("######################################")

# 조건 찾기

# 행 찾기
print(df['과목명'] == '경영학', end='\n\n') # 과목명이 경영학인 모든 행에 대해 true/false 값을 출력
print(df.loc[df['과목명'] == '경영학'], end='\n\n') # 과목명이 경영학인 행 전체를 출력
print(df.loc[df['시간수'] > 2], end='\n\n') # 시간수가 2보다 큰 행을 출력

# 셀 찾기
print(df.loc[df['과목명'] == '경영학']['담당교수'], end='\n\n') # 과목명이 경영학인 행의 담당교수 열의 값을 출력
print(df.loc[df['과목명'] == '경영학']['담당교수'].values[0], end='\n\n') # 과목명이 경영학인 행에서 첫 번째 담당교수의 값을 출력

# df.loc[3]['담당교수'] = '이경영'
df.loc[3, '담당교수'] = '이경영' # 3번째 행의 담당교수 값을 이경영으로 수정
print(df, end='\n\n')

# df.loc[df['과목명'] == '경영학']['담당교수'] = '이경영' # <- 이 방식은 복사본에 변경하는 것이므로 본래 데이터프레임 값에 반영이 안 될 수 있음.
df.loc[df['과목명'] == '경영학', '담당교수'] = '이경영' # 과목명이 경영학인 행의 담당교수 값을 모두 이경영으로 수정
