data = {
    '과목번호' : ['C1', 'C2', 'C3', 'C4', 'C5', 'C6'],
    '과목명' : ['인공지능개론', '웃음치료', '경영학', '3D디자인', '스포츠경영', '예술의 세계'],
    '강의실' : ['R1', 'R2', 'R3', 'R4', 'R2', 'R3'],
    '시간수' : [3, 2, 3, 4, 2, 1]
}

# 데이터 프레임을 생성 `data'는 미리 정의된 데이터
df = pd.DataFrame(data)  

# 데이터 프레임의 내용을 출력
print(df, end='\n\n')  

# 데이터 프레임에서 '과목명'이라는 열을 선택 후 시리즈(sr_name)로 만듦
sr_name = df['과목명']  

# 시리즈 'sr_name'의 내용을 출력
print(sr_name, end='\n\n')  

# 데이터 프레임의 3번째 행(인덱스 2)을 선택하여 시리즈(sr_no)로 만듭니다. 각 열의 데이터가 포함됩니다.
sr_no = df.loc[2]  

# 시리즈 'sr_no'의 내용을 출력
print(sr_no, end='\n\n')  

# 데이터 프레임의 3번째 행(인덱스 2)에서 '과목명' 열의 값을 선택하여 출력
cell_name = df.loc[2]['과목명']  

# 선택한 '과목명'의 값을 출력
print(cell_name)  

# 데이터 프레임을 CSV 파일로 저장 'file.csv'라는 파일명으로 저장, 인덱스는 저장하지 않음
df.to_csv('file.csv', index=False)  

print("######################################")

#df[''] 열생성 (ㅣ)
df['담당교수'] = ['홍길동', '김철수', '이영희', '박영수', '최영희', '김영수']
print(df, end='\n\n')
#df.loc[] 행생성 (-)
